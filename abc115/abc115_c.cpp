using namespace std;
int main(void){
    int n, k, ans = 1e9;
    cin >> n >> k;
    vector<int> h(n);
    for(int i = 0; i < n; i++) cin >>  h[i];

    sort(h.begin(), h.end());
    for(int i = 0; i <= n-k; i++){
        if(h[i+k-1] - h[i] <= ans) ans = h[i+k-1] - h[i];
    }
    cout << ans << endl;

}